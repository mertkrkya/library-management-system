    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
        mesaj_yazisi.setText("");
        String islem_no = no_alani.getText();
        String kitap_no = kitap_alani.getText();
        String uye_no = uye_alani.getText();
        String alim_tarihi = alim_alani.getText();
        String teslim_tarihi = teslim_alani.getText();
        baglanti.oduncEkle(islem_no,kitap_no,uye_no,alim_tarihi,teslim_tarihi);
        oduncGoruntule();
        mesaj_yazisi.setText("Yeni odunc basariyla eklendi...");
    }//GEN-LAST:event_ekleActionPerformed

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
       mesaj_yazisi.setText("");
       
       
       int selectedrow = odunctablo.getSelectedRow();
       
       if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               mesaj_yazisi.setText("Odunc kaydi tablosu şu anda boş...");
           }
           else {
               mesaj_yazisi.setText("Lütfen silinecek bir odunc kaydi seçin...");
           }
      
       }
       else {
           int no_alani = (int)model.getValueAt(selectedrow,0);
           
           baglanti.oduncSil(no_alani);
           
           oduncGoruntule();
           
           mesaj_yazisi.setText("Odunc kaydı başarıyla silindi...");
    }                                   
    }//GEN-LAST:event_silActionPerformed

    /**
     * @param args the command line arguments
     */
        public void dinamikAra(String ara){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        
        
        odunctablo.setRowSorter(tr);
        
        
        tr.setRowFilter(RowFilter.regexFilter(ara));
    }
    private void arama_cubuguKeyReleased(java.awt.event.KeyEvent evt) {                                         
       String ara = arama_cubugu.getText();
       dinamikAra(ara);
    }        
