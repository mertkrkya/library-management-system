    public void oduncGoruntule(){
        model.setRowCount(0);
        ArrayList<Odunc> oduncler = new ArrayList<Odunc>();     
        oduncler = baglanti.oduncleriGetir();
        if (oduncler != null){
            for(Odunc odunc:oduncler){
                Object[] eklenecek = {odunc.getIslemNo(),odunc.getKitapNo(),odunc.getUyeNo(),odunc.getAlimTarihi(),odunc.getTeslimTarihi()};
                model.addRow(eklenecek);
            }
        }
    }
