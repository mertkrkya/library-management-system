    public void uyeGoruntule(){
        model.setRowCount(0);
        ArrayList<Uye> uyeler = new ArrayList<Uye>();     
        uyeler = baglanti.uyeleriGetir();
        if (uyeler != null){
            for(Uye uye:uyeler){
                Object[] eklenecek = {uye.getUyeNo(),uye.getUyeAd(),uye.getUyeSoyad(),uye.getUyeCinsiyet(),uye.getUyeTelefon(),uye.getUyeAdres()};
                model.addRow(eklenecek);
                        
            }
        }
    }
