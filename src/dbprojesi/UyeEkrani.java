    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
        mesaj_yazisi.setText("");
        String uye_no = no_alani.getText();
        String uye_ad = ad_alani.getText();
        String uye_soyad = soyad_alani.getText();
        String uye_cinsiyet = cinsiyet_alani.getText();
        String uye_telefon = telefon_alani.getText();
        String uye_adres = adres_alani.getText();
        baglanti.uyeEkle(uye_no,uye_ad,uye_soyad,uye_cinsiyet,uye_telefon,uye_adres);
        uyeGoruntule();
        mesaj_yazisi.setText("Yeni üye basariyla eklendi...");        
    }//GEN-LAST:event_ekleActionPerformed

    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleActionPerformed
        mesaj_yazisi.setText("");
        String uye_ad = ad_alani.getText();
        String uye_soyad = soyad_alani.getText();
        String uye_cinsiyet = cinsiyet_alani.getText();
        String uye_telefon = telefon_alani.getText();
        String uye_adres = adres_alani.getText();
        int selectedrow = uyetablo.getSelectedRow();
        if(selectedrow==-1){            
            if(model.getRowCount()==0){
                mesaj_yazisi.setText("Uye tablosu şuan boş...");
            }
            else{
                mesaj_yazisi.setText("Lütfen güncellenecek başka uye seçiniz...");
            }
        }
        else{
            int uye_no = (int)model.getValueAt(selectedrow,0);
            baglanti.uyeGuncelle(uye_no,uye_ad,uye_soyad,uye_cinsiyet,uye_telefon,uye_adres);
            uyeGoruntule();
            mesaj_yazisi.setText("Üye başarıyla güncellendi...");
        }
    }//GEN-LAST:event_guncelleActionPerformed

    private void uyetabloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_uyetabloMouseClicked
        int selectedrow = uyetablo.getSelectedRow();
        ad_alani.setText(model.getValueAt(selectedrow,1).toString());
        soyad_alani.setText(model.getValueAt(selectedrow,2).toString());
        cinsiyet_alani.setText(model.getValueAt(selectedrow,3).toString());
        telefon_alani.setText(model.getValueAt(selectedrow,4).toString());
        adres_alani.setText(model.getValueAt(selectedrow,5).toString());    
    }//GEN-LAST:event_uyetabloMouseClicked

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
       mesaj_yazisi.setText("");
       
       
       int selectedrow = uyetablo.getSelectedRow();
       
       if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               mesaj_yazisi.setText("Uye tablosu şu anda boş...");
           }
           else {
               mesaj_yazisi.setText("Lütfen silinecek bir uye seçin...");
           }
      
       }
       else {
           int uye_no = (int)model.getValueAt(selectedrow,0);
           
           baglanti.uyeSil(uye_no);
           
           uyeGoruntule();
           
           mesaj_yazisi.setText("Üye başarıyla silindi...");
    }//GEN-LAST:event_silActionPerformed
    }
    /**
     * @param args the command line arguments
     */
        public void dinamikAra(String ara){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        
        
        uyetablo.setRowSorter(tr);
        
        
        tr.setRowFilter(RowFilter.regexFilter(ara));
    }
    private void arama_cubuguKeyReleased(java.awt.event.KeyEvent evt) {                                         
       String ara = arama_cubugu.getText();
       dinamikAra(ara);
    }        
